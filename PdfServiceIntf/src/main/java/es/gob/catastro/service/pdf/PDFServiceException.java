package es.gob.catastro.service.pdf;

public class PDFServiceException  extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public PDFServiceException() {
		super();
	}

	public PDFServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PDFServiceException(String arg0) {
		super(arg0);
	}

	public PDFServiceException(Throwable arg0) {
		super(arg0);
	}
	
	

}
